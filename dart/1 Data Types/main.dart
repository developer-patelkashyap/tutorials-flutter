/// Data Types

main() {
    int amount1 = 100;
    var amount2 = 200;

    print('amount1: $amount1 | amount2: $amount2');

    double dAmount1 = 100.11;
    var dAmount2    = 200.22;

    print('dAmount1: $dAmount1 | dAmount2: $dAmount2');

    String fname = 'Kashyap';
    var lname    = 'Patel';

    print('My name is $fname $lname.');

    bool isItTrue1 = true;
    var isItTrue2  = false;
    
    print('isItTrue1: $isItTrue1 | isItTrue2: $isItTrue2');

    dynamic dVariable = 100;
    print('dVariable: $dVariable');

    dVariable = 'Dart Programming';
    print('dVariable: $dVariable');
}
